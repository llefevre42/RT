#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned const char		*src1;
	unsigned const char		*src2;
	size_t					i;

	i = 0;
	src1 = (unsigned const char*)s1;
	src2 = (unsigned const char*)s2;
	while (i < n)
	{
		if (src1[i] != src2[i])
			return (src1[i] - src2[i]);
		i++;
	}
	return (0);
}
