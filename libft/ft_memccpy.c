#include "libft.h"

void	*ft_memccpy(void *dst, const void *src,
		int c, size_t n)
{
	int					i;
	unsigned char		*d;
	unsigned const char	*s;

	i = 0;
	d = (unsigned char*)dst;
	s = (unsigned const char*)src;
	while (n--)
	{
		d[i] = s[i];
		if (s[i] == (unsigned char)c)
		{
			return (d + i + 1);
		}
		i++;
	}
	return (NULL);
}
